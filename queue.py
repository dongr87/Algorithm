#!/usr/bin/env python3
# -*- coding: utf-8 -*-


class Queue:
    def __init__(self):
        self.items = []

    def enqueue(item):
        self.items.insert(0, item)

    def dequeue():
        self.items.pop()

    def isEmpty():
        return self.items == []

    def size():
        return len(self.items)
